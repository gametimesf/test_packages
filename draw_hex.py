def draw_shape(num_sides):
  if num_sides == 1:
    print('-')
  if num_sides == 2:
    print('L')
  if num_sides == 3:
    print('∆')
  if num_sides == 4:
    print('□')
  if num_sides == 5:
    print('⬠')
  if num_sides == 6:
    print("""
  _____
 /     \ 
/       \ 
\       /
 \_____/
 \n
 """)
